def generate_random_tricks(props_list):
    result = []

    for key, length_of_props in props_list:
        if key in SITESTWAPS and len(SITESTWAPS[key]) >= length_of_props:
            sampled_values = random.sample(SITESTWAPS[key], length_of_props)
        else:
            sampled_values = SITESTWAPS.get(key, [])[:length_of_props]

        result.append((key, sampled_values))

    return sorted(result)